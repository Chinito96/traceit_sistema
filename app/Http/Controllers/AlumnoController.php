     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $newAlumno = new Alumno;
        $newAlumno->nombre = $request->nombre;
        $newAlumno->apellidos = $request->apellidos;
        $newAlumno->grado = $request->grado;
        $newAlumno->fecha_nacimiento = $request->fecha_nacimiento;

        $newAlumno->save();

        return redirect('/')->with('message', 'Se ha registrado un nuevo alumno');
    }

